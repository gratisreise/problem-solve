import java.util.*;

class Solution {
    int MOD = 20170805;

    public int solution(int m, int n, int[][] cityMap) {
        // dp[i][j][0]: (i, j)에 "위에서 아래로" 들어온 경로 수
        // dp[i][j][1]: (i, j)에 "왼쪽에서 오른쪽으로" 들어온 경로 수
        int[][][] dp = new int[m][n][2];

        // 1. 시작점 초기화
        // dp[0][0][0]을 1로 두면, 아래쪽/오른쪽 첫 이동 시 각각 1씩 전달됩니다.
        dp[0][0][0] = 1;
        dp[0][0][1] = 0; // 혹은 둘 중 하나만 1로 두면 됨 (합산 시 1이 되도록)

        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                // 출발점은 통과 (이미 초기화됨)
                if (i == 0 && j == 0) continue;
                
                // 현재 칸이 벽(1)이면 못 지나감
                if (cityMap[i][j] == 1) continue;

                // 2. 위에서 아래로 내려오는 경우 (dp[i][j][0])
                if (i > 0) {
                    // 바로 위 칸(i-1, j)의 상태를 체크
                    if (cityMap[i - 1][j] == 0) {
                        // 위 칸이 자유(0)면: 위 칸에 도달한 모든 방향의 합
                        dp[i][j][0] = (dp[i - 1][j][0] + dp[i - 1][j][1]) % MOD;
                    } else if (cityMap[i - 1][j] == 2) {
                        // 위 칸이 직진(2)면: 위에서 내려오던 것만 그대로 상속
                        dp[i][j][0] = dp[i - 1][j][0];
                    }
                    // 위 칸이 1이면 자동으로 0 유지
                }

                // 3. 왼쪽에서 오른쪽으로 오는 경우 (dp[i][j][1])
                if (j > 0) {
                    // 바로 왼쪽 칸(i, j-1)의 상태를 체크
                    if (cityMap[i][j - 1] == 0) {
                        // 왼쪽 칸이 자유(0)면: 왼쪽 칸에 도달한 모든 방향의 합
                        dp[i][j][1] = (dp[i][j - 1][0] + dp[i][j - 1][1]) % MOD;
                    } else if (cityMap[i][j - 1] == 2) {
                        // 왼쪽 칸이 직진(2)면: 왼쪽에서 오던 것만 그대로 상속
                        dp[i][j][1] = dp[i][j - 1][1];
                    }
                }
            }
        }

        // 4. 도착점 (m-1, n-1)에 도달한 두 방향의 경로를 합산하여 반환
        return (dp[m - 1][n - 1][0] + dp[m - 1][n - 1][1]) % MOD;
    }
}